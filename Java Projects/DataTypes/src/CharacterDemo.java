
public class CharacterDemo {

	public static void main(String[] args) {
		
		char CapitalA ='A';
		
		System.out.println("Value is ->" + CapitalA);
		
		int intEquivalent = (int)CapitalA;
		
		System.out.println("Integer Equivalent -> " + intEquivalent);
		
		CapitalA ++;
		
		System.out.println("Value is  -> " + CapitalA);
	}
}
